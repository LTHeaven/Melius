const pixelCount = 800;
const transitionFrames = 45;
const verticeRadius = 8;
var screenScale = 1;
var frame = 0;
var totalFrames = 0;
var verticeOpacity = 0;
var phi = 1.6180339887;
var phidifference = 0.0035;
var state = -3;
var intro;
var title;
var heartSlide;
var graphSlide;
var teamSlide;
var brainSlide;
var recapSlide;
var syringe;
var imageData = {points: new Array(pixelCount), vertices: []};
var currentImage;
var heart = {points: new Array(pixelCount), vertices: []};
var graph = {points: new Array(pixelCount), vertices: []};
var team = {points: new Array(pixelCount), vertices: []};
var brain = {points: new Array(pixelCount), vertices: []};
var recap = {points: new Array(pixelCount), vertices: []};
var atTransitionBegin = {points: new Array(pixelCount), vertices: []};

function scaleCoord(coord) {
    return coord*screenScale;
}

function updateTransitionBegin() {
    imageData.points.forEach(function (point, index) {
       atTransitionBegin.points[index] = {
           x: point.x,
           y: point.y
       };
    });
    atTransitionBegin.vertices = [...imageData.vertices];
}

function initFor(pattern, target, x1, y1, width, height) {
    for (var i = 0; i < 800; i++){
        patternPoint = pattern.points[i%pattern.points.length];
        setPixel(i, x1+width*patternPoint.x, y1+height*patternPoint.y, target.points);
    }
    pattern.vertices.forEach(function (vertice) {
       target.vertices.push({
           point1: {
               x: x1 + width*vertice.point1.x,
               y: y1 + width*vertice.point1.y
           },
           point2: {
               x: x1 + width*vertice.point2.x,
               y: y1 + width*vertice.point2.y
           }
       });
    });
    target.points = shuffle(target.points);
}

function initVertices(data) {
    data.vertices.forEach(function (vertice){
        vertice.offset = random(2*PI);
    });
}

function initSlides() {
    initFor(heartPattern, heart, scaleCoord(50), scaleCoord(50), scaleCoord(620), scaleCoord(620));
    initFor(graphPattern, graph, scaleCoord(100), scaleCoord(100), scaleCoord(520), scaleCoord(520));
    initFor(teamPattern, team, scaleCoord(0), scaleCoord(0), scaleCoord(720), scaleCoord(720));
    initFor(brainPattern, brain, scaleCoord(0), scaleCoord(50), scaleCoord(570), scaleCoord(570));
    initFor(recapPattern, recap, scaleCoord(0), scaleCoord(00), scaleCoord(720), scaleCoord(720));
    initVertices(heart);
    initVertices(graph);
    initVertices(team);
    initVertices(brain);
    initVertices(recap);
}

function preload() {
    heartSlide = loadImage("./images/heartSlide.png");
    graphSlide = loadImage("./images/graphSlide.png");
    teamSlide = loadImage("./images/teamSlide.png");
    brainSlide = loadImage("./images/brainSlide.png");
    recapSlide = loadImage("./images/recapSlide.png");
    title = loadImage("./images/title.png");
    intro = loadImage("./images/intro.png");
    syringe = loadImage("./images/syringe.png");

}

function setup() {
    createCanvas(scaleCoord(1280), scaleCoord(720));
    stroke(255);
    fill(0);
    frameRate(30);
    initSlides();
}

function getAngle(){
    if (frame*8 < 800){
        return ((frame*8)/800 * phidifference) + (phi-phidifference);
    } else{
        return phi;
    }
}
function setPixel(index, x, y, array){
    array[index] = {
        x: x,
        y: y
    };
}

function drawIntro(){
    if (frame < transitionFrames/2){
        tint(255);
        let factor = 1-(frame/transitionFrames-0.1)*10;
        tint(255*factor);
        image(syringe, scaleCoord(340), scaleCoord(720/2-110/2));
    }
    for (var i = 0; i<frame*8 && i < 800; i++){
        x = i * cos(getAngle()*2*PI*i);
        y = i * sin(getAngle()*2*PI*i);
        setPixel(i, x + scaleCoord(680), y + scaleCoord(360), imageData.points);
    }
    if (frame*8 > 830) {
        state = 1;
        frame = 0;
        updateTransitionBegin();
        imageData.points = shuffle(imageData.points);
    }
}

function titelPage() {
    currentImage = title;
    verticeOpacity = 1;
    if(keyIsPressed){
        state = -2;
        frame = 0;
    }
}

function motivation() {
    if (frame > transitionFrames/2){
        currentImage = intro;
        verticeOpacity = (frame/transitionFrames-0.75)*4;
        tint(255*verticeOpacity,255*verticeOpacity,255*verticeOpacity);
        image(syringe, 100, 720/2-110/2);
    }else if (frame < transitionFrames/2 && frame < transitionFrames){
        verticeOpacity = 1-(frame/transitionFrames-0.1)*10;
    }
    if(frame > transitionFrames){
        image(syringe, 100, 720/2-110/2);
        if (keyIsPressed){
            state = -1;
            frame = 0;
        }
    }
}

function inject() {
    if (frame < transitionFrames/2){
        verticeOpacity = 1-(frame/transitionFrames-0.1)*10;
    }
    tint(255);
    image(syringe, scaleCoord(100) + scaleCoord((540-300)) * ((cos(PI + PI*(frame/(transitionFrames/2)))+1)/2), scaleCoord(720/2-110/2));
    if (frame > transitionFrames/2){
        state = 0;
        frame = 0;
        currentImage = undefined;
    }
}

function draw() {
    frame++;
    totalFrames++;
    clear();
    background(0,0,0);
    switch (state) {
        case -3:
            titelPage();
            break;
        case -2:
            motivation();
            break;
        case -1:
            inject();
            break;
        case 0:
            drawIntro();
            break;
        case 1:
            transition(heart, heartSlide, 2);
            break;
        case 2:
            transition(graph, graphSlide, 3);
            break;
        case 3:
            transition(team, teamSlide, 4);
            break;
        case 4:
            transition(brain, brainSlide, 5);
            break;
        case 5:
            transition(recap, recapSlide, 6);
            break;

    }
    fill(255);
    imageData.points.forEach(function (pixel, index) {
        circle(scaleCoord(pixel.x), scaleCoord(pixel.y),scaleCoord(3));
    });
    imageData.vertices.forEach(function (vertice) {
        stroke(verticeOpacity*150 + 105*((sin(vertice.offset + totalFrames/5)+1)/2));

        strokeWeight(3);
        let x1 = vertice.point1.x;
        let y1 = vertice.point1.y;
        let x2 = vertice.point2.x;
        let y2 = vertice.point2.y;
        line(
            x1 + ((x2-x1)/sqrt(pow(x2-x1, 2) + pow(y2-y1,2))) * verticeRadius,
            y1 + ((y2-y1)/sqrt(pow(x2-x1, 2) + pow(y2-y1,2))) * verticeRadius,
            x2 + ((x1-x2)/sqrt(pow(x1-x2, 2) + pow(y1-y2,2))) * verticeRadius,
            y2 + ((y1-y2)/sqrt(pow(x1-x2, 2) + pow(y1-y2,2))) * verticeRadius
        );
        stroke(255);
        strokeWeight(1);
    });
    tint(255*verticeOpacity,255*verticeOpacity,255*verticeOpacity);
    if(typeof currentImage !== 'undefined'){
        image(currentImage, 0, 0, scaleCoord(1280), scaleCoord(720));
    }
}

function transition(target, slide, stateAfterCompletion){
    if(frame < transitionFrames){
        let transitionFactor = frame / transitionFrames;
        for (var i = 0; i<pixelCount; i++){
            originalX = atTransitionBegin.points[i].x;
            originalY = atTransitionBegin.points[i].y;
            targetX = target.points[i].x;
            targetY = target.points[i].y;
            imageData.points[i].x = originalX + (targetX - originalX)*((cos(PI + PI*transitionFactor)+1)/2);
            imageData.points[i].y = originalY + (targetY - originalY)*((cos(PI + PI*transitionFactor)+1)/2);
        }
        if (frame > transitionFrames/2){
            imageData.vertices = target.vertices;
            currentImage = slide;
            verticeOpacity = (frame/transitionFrames-0.75)*4;
        }
        if (frame < transitionFrames/2){
            verticeOpacity = 1-(frame/transitionFrames-0.1)*10;
        }
    }else{
        if(keyIsPressed){
            state = stateAfterCompletion;
            frame = 0;
            updateTransitionBegin();
        }
    }
}


/**
 * Shuffles array in place.
 * @param {Array} a items An array containing the items.
 */
function shuffle(a) {
    var j, x, i;
    for (i = a.length - 1; i > 0; i--) {
        j = Math.floor(Math.random() * (i + 1));
        x = a[i];
        a[i] = a[j];
        a[j] = x;
    }
    return a;
}


